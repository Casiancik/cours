SKILLS = [
    "Стремительный прыжок",
    "Электрический выстрел",
    "Ледяной удар",
    "Стремительный удар",
    "Кислотный взгляд",
    "Тайный побег",
    "Ледяной выстрел",
    "Огненный заряд",
]


def get_runic_skill():
    word = ""
    runic_skill = []
    for word in SKILLS:
        new_word = word
        for character in word:
            runic_character = RUNIC_LETTERS.get(character, character)
            new_word = new_word.replace(character, runic_character)
        runic_skill.append(new_word)
    return runic_skill


def user_generate():
    fake = Faker("ru_RU")
    skill_1, skill_2, skill_3 = random.sample(get_runic_skill(), 3)
    context = {
        "first_name": fake.first_name(),
        "last_name": fake.last_name(),
        "job": fake.job(),
        "town": fake.city(),
        "strength": random.randint(3, 18),
        "agility": random.randint(3, 18),
        "endurance": random.randint(3, 18),
        "intelligence": random.randint(3, 18),
        "luck": random.randint(3, 18),
        "skill_1": skill_1,
        "skill_2": skill_2,
        "skill_3": skill_3, 
    }
    return context


def generate_cards():
    new_path = f"game"
    os.makedirs(new_path, exist_ok=True)
    new_name = ""
    for new_name in range(1, 11):
        card = file_operations.render_template("game.svg", "{0}/new_game_{1}.svg".format(new_path, new_name), user_generate())
    return card


